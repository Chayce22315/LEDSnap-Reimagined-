void ls_text_init(LSTextElement *elem, const char *text,
                  float speed, LSPixel color, uint16_t y) {
    elem->text        = text;
    elem->x_offset    = 0.0f;
    elem->scroll_speed = speed;
    elem->color       = color;
    elem->font_height = LS_FONT_CHAR_H;
    elem->y_position  = y;
    elem->wrap        = 1;
}

void ls_text_update(LSTextElement *elem, float delta_time, uint16_t grid_width) {
    elem->x_offset += elem->scroll_speed * delta_time;

    if (elem->wrap && elem->text) {
        size_t len = strlen(elem->text);
        float total_w = (float)(len * (LS_FONT_CHAR_W + 1));

        if (elem->scroll_speed < 0 && elem->x_offset < -total_w)
            elem->x_offset = (float)grid_width;
        else if (elem->scroll_speed > 0 && elem->x_offset > (float)grid_width)
            elem->x_offset = -total_w;
    }
}

void ls_text_render(const LSTextElement *elem, LSGrid *target) {
    if (!elem->text) return;

    int cx = (int)elem->x_offset;
    size_t len = strlen(elem->text);

    for (size_t ci = 0; ci < len; ci++) {
        unsigned char ch = (unsigned char)elem->text[ci];
        if (ch < LS_FONT_FIRST || ch > LS_FONT_LAST)
            ch = ' ';

        const uint8_t *glyph = ls_font_5x7[ch - LS_FONT_FIRST];

        for (int row = 0; row < LS_FONT_CHAR_H; row++) {
            int gy = elem->y_position + row;
            if (gy < 0 || gy >= target->height) continue;

            uint8_t bits = glyph[row];
            for (int col = 0; col < LS_FONT_CHAR_W; col++) {
                int gx = cx + col;
                if (gx < 0 || gx >= target->width) continue;

                if (bits & (0x80 >> col)) {
                    *ls_grid_at(target, (uint16_t)gx, (uint16_t)gy) = elem->color;
                }
            }
        }
        cx += LS_FONT_CHAR_W + 1; /* 1px spacing between glyphs */
    }
}
